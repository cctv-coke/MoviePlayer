def print_api_status():
    print("\n" + "="*80)
    print(" "*30 + "📡 API源状态")
    print("="*80)
    
    from core.api import MovieAPI
    api = MovieAPI()
    
    print("\n正在测试API连接...")
    result = api.get_category_list('movie', 1)
    
    if result['list']:
        print(f"\n✅ 百度云资源 - 正常")
        print(f"   获取到 {len(result['list'])} 条数据")
        print(f"\n📺 最新影视:")
        for i, movie in enumerate(result['list'][:5], 1):
            print(f"   {i}. {movie['name']} ({movie['type']}) - {movie.get('remarks', 'N/A')}")
    else:
        print("\n⚠️  API连接异常")

